task handleDrive()
{
	while(true)
	{
		motor[leftBackDrive] = vexRT[Ch3];
		motor[leftFrontDrive] = vexRT[Ch3];
		motor[rightBackDrive] = vexRT[Ch2];
		motor[rightFrontDrive] = vexRT[Ch2];
	}
}

task handleLift()
{
	while(true)
	{
		if(vexRT[Btn5U])
		{
			while(!SensorValue[liftUpBtn])
			{
				motor[leftLift] = 69;
				motor[rightLift] = 69;
			}
		}
		if(vexRT[Btn5D])
		{
			while(!SensorValue[liftDownBtn])
			{
				motor[leftLift] = -69;
				motor[rightLift] = -69;
			}
		}
	}

}

task handleIntake()
{
	while(true)
	{
		if(vexRT[Btn6U])
		{
			motor[intakeLeft] = 69;
			motor[intakeRight] = 69;
			wait1Msec(1000);
			motor[intakeLeft] = 0;
			motor[intakeRight] = 0;
			
			while(!SensorValue[intakeDownBtn])
			{
				motor[intakeLeft] = -69;
				motor[intakeRight] = -69;
			}
		}
	}
}

task usercontrol()
{
	// User control code here, inside the loop
	StartTask(handleDrive);
	StartTask(handleLift);
	StartTask(handleIntake);
}